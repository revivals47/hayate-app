// Language switching
function setLang(lang) {
    const t = translations[lang] || translations.en;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.innerHTML = t[key];
            }
        }
    });
    document.documentElement.lang = lang;
    localStorage.setItem('hayate-lang', lang);
    // Update active state in language selector
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('text-white', btn.dataset.lang === lang);
        btn.classList.toggle('text-gray-500', btn.dataset.lang !== lang);
    });
}

// Auto-detect language or use saved preference
function initLang() {
    const saved = localStorage.getItem('hayate-lang');
    if (saved && translations[saved]) { setLang(saved); return; }
    const browserLang = navigator.language || navigator.userLanguage || 'en';
    if (browserLang.startsWith('ja')) { setLang('ja'); }
    else if (browserLang.startsWith('ko')) { setLang('ko'); }
    else if (browserLang.startsWith('zh-Hant') || browserLang === 'zh-TW' || browserLang === 'zh-HK') { setLang('zh-Hant'); }
    else if (browserLang.startsWith('zh')) { setLang('zh-Hans'); }
    else { setLang('en'); }
}
